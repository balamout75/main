public class AnswerProperty  {
    private String      Value;
    private int         PropertyIndex ;
    private AnswerInterface    AI ;

    public AnswerProperty (int PropertyIndex, String Value, AnswerInterface AI) {
        this.Value=Value;
        this.PropertyIndex=PropertyIndex;
        this.AI=AI;
    }

    public String getValue() {
        return Value;
    }

    public void setValue(String Value) {

        try {
            switch (this.getProperyIndex()) {
                case 2:{
                    AI.setTextable(Value.equalsIgnoreCase("true"), true);
                    //QI.setBase(new Integer(Value));
                    this.Value=Value;
                    break;
                }
                case 3:{
                    //QI.setMaxAnswerCount(new Integer(Value));
                    this.Value=Value;
                    break;
                }
              }
        } catch (Exception E) {
            System.out.println("Ошибка в определении свойств QuestionProperty "+E);
        }
    }

    public String toString() {
        String Header="";
        switch (this.getProperyIndex()) {
                case 1:{
                    Header="Свойства ответа ";
                    break;
                }
                case 2:{
                    Header="Возможность текстового ответа ";
                    break;
                }
                case 3:{
                    Header="Максимальное число ответов ";
                    break;
                }
                case 4:{
                    Header="Тип переменной ";
                    break;
                }
              }
        return Header+Value;
    }

    public int getProperyIndex() {
        return PropertyIndex;
    }

}

public class CATreeCellRenderer extends DefaultTreeCellRenderer {
    ImageIcon Icon1 = new ImageIcon("images/gif/18x16/08.gif");
    ImageIcon Icon2 = new ImageIcon("images/gif/18x16/01.gif");
    ImageIcon Icon3 = new ImageIcon("images/gif/18x16/10.gif");
    ImageIcon Icon4 = new ImageIcon("images/gif/18x16/09.gif");

    private DefaultTreeCellRenderer nonLeafRenderer = new DefaultTreeCellRenderer();
    public CATreeCellRenderer() {
    }
    public java.awt.Component getTreeCellRendererComponent(JTree tree, Object value,
						  boolean sel,
						  boolean expanded,
						  boolean leaf, int row,
						  boolean hasFocus) {
        java.awt.Component returnValue;
        ImageIcon icon=null;
        try {
            AnswerProperty AP=(AnswerProperty)((DefaultMutableTreeNode)value).getUserObject();
            switch (AP.getProperyIndex()) {
                case 1:{
                    icon=Icon1;
                    break;
                }
                case 2:{
                    icon=Icon2;
                    break;
                }
                case 3:{
                    icon=Icon3;
                    break;
                }
              }
        } catch (Exception E) {
            System.out.println("Ошибка в назначении иконки"+E);
        }
        if (icon!=null) {
            nonLeafRenderer.setOpenIcon(icon);
            nonLeafRenderer.setClosedIcon(icon);
            nonLeafRenderer.setLeafIcon(icon);
        }
        returnValue = nonLeafRenderer.getTreeCellRendererComponent(tree,value, sel, expanded, leaf, row, hasFocus);
        return returnValue;
    }
    }

