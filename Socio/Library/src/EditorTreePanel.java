    public void setSelectionRows(int [] rows){
        try {
            JTreeFocus=false;
            System.out.println("Что то выделяем )) ");
            int                  numRows = rows.length;
            for(int counter = 0; counter < numRows; counter++) {
                    if (rows[counter]!=0) {
                        DefaultMutableTreeNode root=(DefaultMutableTreeNode) jTree1.getPathForRow(rows[counter]).getLastPathComponent();
                        CheckBoxAnswerMark AM= (CheckBoxAnswerMark) root.getUserObject();
                        AM.setSelected(true);
                        if (QI.getQuestionType()==alter) {
                            TCE.lastCheckBoxNode=AM;
                        } else {
                            TCE.AnswerCount = numRows;
                        }
                    }
            }
            JTreeFocus=true;
        } catch( Exception e )	{
            System.out.println( e.toString() );
        }
    }
    
