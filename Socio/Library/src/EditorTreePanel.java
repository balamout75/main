    public void clearSelection(CheckBoxAnswerMark checkBoxExcept) {
        try {
            DefaultMutableTreeNode DMT=(DefaultMutableTreeNode)jTree1.getModel().getRoot();
            for (int i = 0; i < QI.getSize(); i++) {
                DefaultMutableTreeNode branch=(DefaultMutableTreeNode)DMT.getChildAt(i);
                CheckBoxAnswerMark checkBoxNode = (CheckBoxAnswerMark)branch.getUserObject();
                if (checkBoxExcept!=null) {
                    if (!checkBoxNode.RAI.equals(checkBoxExcept.RAI)) {
                        if (checkBoxNode.isSelected()) {
                            checkBoxNode.setSelected(false);
                            jTree1.updateUI();
                        }
                    }
                } if (checkBoxNode.isSelected()) checkBoxNode.setSelected(false);
            }
        } catch (Exception ex) {
                System.out.println("Ошибка в ETP clearSelection"+ex.toString());
        }
    }


    public void getCheckedNodes() {
        //ArrayList Res = new ArrayList();
        try {
            DefaultMutableTreeNode DMT=(DefaultMutableTreeNode)jTree1.getModel().getRoot();
            String ValStr="";
            String Pos="";
            for (int i = 0; i < QI.getSize(); i++) {
                DefaultMutableTreeNode branch=(DefaultMutableTreeNode)DMT.getChildAt(i);
                CheckBoxAnswerMark checkBoxNode = (CheckBoxAnswerMark)branch.getUserObject();
                if (checkBoxNode.isSelected()) {
                    ValStr=ValStr+Pos+(i+1);
                    Pos=" ";
                }
            }
            jTree1.updateUI();
            Lib.CurrentInputPanel.setValue(ValStr);
        } catch (Exception ex) {
            System.out.println("Ошибка в ETP getCheckedNodes"+ex.toString());
        }
        //return Res;
    }

